// Helper function to get abbreviation from full name
export const getProductAbbreviation = (fullName: string): string => {
  return productAbbreviations[fullName] || fullName.substring(0, 2).toUpperCase();
};

// Helper function to get full name from abbreviation
export const getProductFullName = (abbreviation: string): string | undefined => {
  return Object.entries(productAbbreviations).find(([_, abbr]) => abbr === abbreviation)?.[0];
}; 